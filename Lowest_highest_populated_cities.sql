-- Solution

With CTE as(
Select *,
Max(population) over (partition by state) as max_pop,
Min(population) over (partition by state) as min_pop
from city_population)

Select state,
max(case when population = max_pop then city end) as highest_populated,
min(case when population = min_pop then city end) as lowest_populated
from CTE
group by state